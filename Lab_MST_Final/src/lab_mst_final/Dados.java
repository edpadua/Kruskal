package lab_mst_final;


public class Dados 
{
    public int cid1, cid2;
    public double dist;

    public void setCid1(int cid1)
    {
        this.cid1 = cid1;
    }

    public void setCid2(int cid2)
    {
        this.cid2 = cid2;
    }

    public void setDist(double dist)
    {
        this.dist = dist;
    }

    public int getCid1()
    {
        return cid1;
    }

    public int getCid2()
    {
        return cid2;
    }

    public double getDist()
    {
        return dist;
    }
}
